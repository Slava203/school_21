int	ft_sqrt(int nb)
{
	int i;
	int next_odd;

	i = 0;
	next_odd = 1;
	while (nb > 0)
	{
		nb = nb - next_odd;
		i++;
		next_odd += 2;
	}
	if (nb == 0)
		return (i);
	else
		return (0);
}
